// ============================================================================
// INCIDENT
// ============================================================================

/**
 * Incident model
 * 
 * Represents a deduplicated alert instance.
 */
export interface Incident {
  /** Unique incident ID */
  incidentId: string;
  /** Alert key (dedupe key) */
  alertKey: string;
  /** Correlation ID */
  correlationId: string;
  /** Alert type */
  alertType: AlertType;
  /** Alert category */
  category: AlertCategory;
  /** Alert severity */
  severity: AlertSeverity;
  /** Tenant scope */
  tenantScope: TenantScope;
  /** Tenant ID (single_tenant ise) */
  tenantId?: string;
  /** Incident status */
  status: IncidentStatus;
  /** Created timestamp */
  createdAt: string;
  /** Updated timestamp */
  updatedAt: string;
  /** Resolved timestamp */
  resolvedAt?: string;
  /** Resolution details */
  resolution?: IncidentResolution;
  /** Aggregated alert count */
  alertCount: number;
  /** Last alert timestamp */
  lastAlertAt: string;
  /** Source component */
  component: string;
}

/**
 * Incident resolution details
 * 
 * @see Requirements 8.1-8.3
 */
export interface IncidentResolution {
  /** Resolution reason */
  reason: ResolutionReason;
  /** Root cause hint */
  rootCauseHint?: string;
  /** Resolved by (actor ID) */
  resolvedBy?: string;
  /** Duration in milliseconds */
  durationMs: number;
}

// ============================================================================
// GLOBAL OUTAGE
// ============================================================================

/**
 * Global outage model
 * 
 * @see Requirements 17.4
 */
export interface GlobalOutage {
  /** Unique outage ID */
  outageId: string;
  /** Outage reason */
  reason: GlobalOutageReason;
  /** Affected categories */
  affectedCategories: AlertCategory[];
  /** Started timestamp */
  startedAt: string;
  /** Resolved timestamp */
  resolvedAt?: string;
  /** Outage status */
  status: 'ACTIVE' | 'RESOLVED';
  /** Source incident ID (if escalated from incident) */
  sourceIncidentId?: string;
  /** Declared by (actor ID) */
  declaredBy?: string;
}

// ============================================================================
// SUPPRESSION
