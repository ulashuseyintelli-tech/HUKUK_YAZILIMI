import {
  AlertCategory,
  AlertSeverity,
  AlertStatus,
  AlertType,
  AnomalySeverity,
  GlobalOutageReason,
  IncidentStatus,
  NotificationChannel,
  OwnerTeam,
  ResolutionReason,
  ResourceType,
  SignalCollectorType,
  SuppressionReason,
  TenantScope,
} from '../types/alerting.types';

// ============================================================================
// RAW SIGNALS (from collectors)
// ============================================================================

/**
 * Base raw signal from collectors
 */
export interface RawSignal {
  /** Signal ID (generated by collector) */
  signalId: string;
  /** Timestamp (ISO 8601) */
  timestamp: string;
  /** Source component */
  component: string;
  /** Tenant ID (optional for global signals) */
  tenantId?: string;
  /** Collector type */
  collectorType: SignalCollectorType;
}

/**
 * Security signal from SecurityCollector
 * 
 * @see Requirements 3.1-3.5
 */
export interface SecuritySignal extends RawSignal {
  collectorType: SignalCollectorType.Security;
  signalType: 'JTI_ANOMALY' | 'CROSS_TENANT_ATTEMPT' | 'CROSS_TENANT_BLOCKED';
  anomalyType?: 'HIGH_USAGE' | 'MULTI_ACTOR';
  severity: AnomalySeverity;
  jti?: string;
  grantId?: string;
  actorCount?: number;
  targetTenantId?: string;
}

/**
 * Health signal from HealthCollector
 * 
 * @see Requirements 1.1-1.5, 2.1-2.4
 */
export interface HealthSignal extends RawSignal {
  collectorType: SignalCollectorType.Health;
  signalType: 'DEGRADED_ENTERED' | 'DEGRADED_EXITED' | 'FAILURE_RECORDED' | 'MANUAL_RESET_REQUIRED';
  degradedDurationMs?: number;
  consecutiveFailures?: number;
  manualResetRequired?: boolean;
}

/**
 * Capacity signal from CapacityCollector
 * 
 * @see Requirements 5.1-5.6
 */
export interface CapacitySignal extends RawSignal {
  collectorType: SignalCollectorType.Capacity;
  signalType: 'RATE_LIMIT_EXHAUSTED' | 'QUEUE_DEPTH_HIGH' | 'RESOURCE_HIGH';
  resourceType?: ResourceType;
  currentValue: number;
  threshold: number;
  limitKey?: string;
  queueName?: string;
}

/**
 * Integrity signal from IntegrityCollector
 * 
 * @see Requirements 6.1-6.3
 */
export interface IntegritySignal extends RawSignal {
  collectorType: SignalCollectorType.Integrity;
  signalType: 'AUDIT_WRITE_FAILURE' | 'STATUS_MISMATCH';
  expectedState?: string;
  actualState?: string;
  errorMessage?: string;
}

/**
 * Hygiene signal from HygieneCollector
 * 
 * @see Requirements 7.1-7.2
 */
export interface HygieneSignal extends RawSignal {
  collectorType: SignalCollectorType.Hygiene;
  signalType: 'VALIDATION_ERROR_SPIKE';
  errorType: string;
  errorCount: number;
  windowMs: number;
  threshold: number;
}

/**
 * Union of all raw signal types
 */
export type AnyRawSignal =
  | SecuritySignal
  | HealthSignal
  | CapacitySignal
  | IntegritySignal
  | HygieneSignal;

// ============================================================================
// NORMALIZED SIGNAL
// ============================================================================

/**
 * Normalized signal - common format for all signals
 * 
 * @see Requirements 15.1-15.7
 */
export interface NormalizedSignal {
  /** Unique signal ID */
  signalId: string;
  /** Signal timestamp (ISO 8601) */
  timestamp: string;
  /** Source component */
  component: string;
  /** Tenant ID (optional - global signals have no tenant) */
  tenantId?: string;
  /** Signal type */
  signalType: string;
  /** Signal category */
  category: AlertCategory;
  /** Dimensions for alertKey generation */
  dimensions: Record<string, string | number>;
  /** Raw severity hint from collector */
  rawSeverityHint?: AlertSeverity;
  /** Evidence reference (audit trail pointer) */
  evidenceRef?: string;
  /** Original raw signal */
  rawSignal: AnyRawSignal;
}

// ============================================================================
// ALERT
// ============================================================================

/**
 * Alert model
 * 
 * @see Requirements 15.1-15.7
 */
export interface Alert {
  /** Unique alert ID */
  alertId: string;
  /** Alert type */
  alertType: AlertType;
  /** Alert category */
  category: AlertCategory;
  /** Alert severity */
  severity: AlertSeverity;
  /** Owner team */
  ownerTeam: OwnerTeam;
  /** Tenant scope */
  tenantScope: TenantScope;
  /** Tenant ID (single_tenant ise) */
  tenantId?: string;
  /** Alert key (dedupe key) */
  alertKey: string;
  /** Correlation ID */
  correlationId: string;
  /** Incident ID */
  incidentId: string;
  /** Recommendation */
  recommendation: string;
  /** Runbook reference */
  runbookRef: string;
  /** Created timestamp */
  createdAt: string;
  /** Updated timestamp */
  updatedAt: string;
  /** Resolved timestamp */
  resolvedAt?: string;
  /** Alert status */
  status: AlertStatus;
  /** Maintenance context */
  maintenanceContext?: boolean;
  /** Suppression context */
  suppressionContext?: SuppressionContext;
  /** Related incident IDs */
  relatedIncidentIds: string[];
}

/**
 * Suppression context for suppressed alerts
 */
export interface SuppressionContext {
  suppressedBy: string;
  parentIncidentId: string;
  reason: SuppressionReason;
}

